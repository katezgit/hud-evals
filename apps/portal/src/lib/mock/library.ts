export type TraceStatus = "running" | "passed" | "failed" | "partial";

export interface TraceRow {
  /** Full trace ID; first 6 hex chars are rendered in the card meta row. */
  id: string;
  status: TraceStatus;
  /** Task slug — `webarena:menu:order-recovery` etc. */
  taskSlug: string;
  /** Parent Job display name (kebab-case, matches the Jobs surface convention). */
  jobName: string;
  jobId: string;
  /** Friendly environment name, not an ECR URI. */
  environment: string;
  /** Model slug routed through HUD Gateway. */
  model: string;
  /** Owner of the parent Job. Matches names used across the mock fixtures. */
  ownerName: string;
  /** Reward 0..1. null while Running. */
  reward: number | null;
  /** Step count — integer, no abbreviation. */
  steps: number;
  /** Exact cost — pre-formatted to four decimals so display stays consistent. */
  cost: string;
  /** Relative timestamp (rendered verbatim on the card; tooltip carries ISO). */
  when: string;
  /** Absolute ISO timestamp used by the tooltip. */
  whenISO: string;
  /** Initial starred state — overlaid by session-scope state in the page. */
  starred: boolean;
  /** Error excerpt for ERRORED rows; absent otherwise. */
  errorExcerpt?: string;
}

